import MarkdownIt from "markdown-it";
import axios from "axios";

export default {
  components: {},
  data() {
    return {
      markdownInput: "",
      renderedMarkdown: "",
      소개란작성모달창: false,
      미리보기모달창: false,
      협업유형도움말모달창: false,
      모달창로고: false,

      //이 부분은 get으로 들고오는 부분
