      Name: "", // 이름
      cooperationType: "", // 협업유형
      onOff: "", // 온/오프라인 여부
      techs: "", // 기술스택
    };
  },
  setup() {},
  created() {},
  mounted() {
    this.fetchData();
  },

  unmounted() {},

  methods: {
    renderMarkdown() {
      const md = new MarkdownIt();

      if (this.markdownInput.length > 0) {
        this.renderedMarkdown = md.render(this.markdownInput);
        this.미리보기모달창 = true;
        this.모달창로고 = false;
      } else {
        alert("최소 1자 이상 작성해주세요.");
      }
    },
    textareaDelete() {
      this.markdownInput = "";
      this.renderedMarkdown = ""; // 미리보기 결과도 초기화
      this.미리보기모달창 = false;
      this.모달창로고 = true;
    },
    writeAndrevise() {
      this.미리보기모달창 = false; // 미리보기 창 닫기
      this.소개란작성모달창 = true;
      this.모달창로고 = true;
    },
    textareaSave() {
      this.소개란작성모달창 = false;
    },

    fetchData() {
      // 서버로 GET 요청을 보냅니다. '/api/userProfile'는 실제 API 엔드포인트에 맞게 변경하세요.
      axios
        .get("http://localhost:3000/user/userProfile", {
          withCredentials: true, // Include cookies with the request
        })
        .then((response) => {
          // 서버에서 받은 데이터를 데이터 변수에 할당합니다.
          const userProfile = response.data;
          console.log(userProfile);
          this.Email = userProfile.email;
          this.Name = userProfile.name;
          this.cooperationType = userProfile.cooperationType;
          this.onOff = userProfile.onOff;
          this.techs = userProfile.techs;
        })
        .catch((error) => {
          console.error("Error fetching user profile:", error);
          // 에러 처리를 수행하세요.
        });
        
    },
    cooperationTypeModal() {
      this.협업유형도움말모달창= true;
  },
  },
};
